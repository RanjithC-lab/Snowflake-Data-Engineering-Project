-- Create TrainData table
CREATE OR REPLACE TABLE TrainData (
    id VARCHAR, 
    Gender VARCHAR, 
    Age INTEGER, 
    Driving_License BOOLEAN, 
    Region_Code VARCHAR, 
    Previously_Insured BOOLEAN, 
    Vehicle_Age VARCHAR, 
    Vehicle_Damage BOOLEAN, 
    Annual_Premium FLOAT, 
    Policy_Sales_Channel INTEGER, 
    Vintage INTEGER, 
    Response BOOLEAN
);
